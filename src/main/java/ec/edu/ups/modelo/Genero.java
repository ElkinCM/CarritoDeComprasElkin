package ec.edu.ups.modelo;

/**
 * Enumeración que representa los géneros posibles de un usuario u otra entidad relacionada.
 *
 * Esta enumeración puede ser utilizada para clasificar personas u objetos según su género.
 * Se incluyen opciones estándar y una opción genérica para incluir otras identidades.
 *
 * @author
 */
public enum Genero {

    /**
     * Género masculino.
     */
    MASCULINO,

    /**
     * Género femenino.
     */
    FEMENINO,

    /**
     * Otros géneros o identidades no especificadas.
     */
    OTROS
}
